function escHtml(s) {
  return String(s ?? "")
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;");
}

function getProductBySku(sku) {
  return PRODUCTS.find(p => p.sku === sku) || PRODUCTS[0];
}

function renderList(activeSku) {
  const list = document.getElementById("products-list");
  if (!list) return;

  list.innerHTML = PRODUCTS.map(p => {
    const active = p.sku === activeSku ? " is-active" : "";
    return `
      <a class="pitem${active}" href="product.html?sku=${encodeURIComponent(p.sku)}">
        <div class="pitem__top">
          <div class="pitem__sku">${escHtml(p.sku)}</div>
          <div class="pitem__brand">${escHtml(p.brand)}</div>
        </div>
        <div class="pitem__name">${escHtml(p.name_ru)}</div>
        <div class="pitem__meta">
          <span>${escHtml(p.color)}</span>
          <span>‚Ä¢</span>
          <span>${escHtml(p.available)}</span>
        </div>
      </a>
    `;
  }).join("");
}

function renderDetails(p) {
  document.title = p.title_ru || p.name_ru;

  const h1 = document.getElementById("product-name");
  const meta = document.getElementById("product-meta");
  const short = document.getElementById("product-short");
  const specs = document.getElementById("product-specs");

  if (h1) h1.textContent = p.h1_ru || p.name_ru;

  if (meta) {
    meta.innerHTML = `
      <div class="chips">
        <span class="chip">${escHtml(p.brand)}</span>
        <span class="chip">${escHtml(p.section)}</span>
        <span class="chip">${escHtml(p.color)}</span>
        <span class="chip">${escHtml(p.available)}</span>
        <span class="chip">${escHtml(p.currency)} ${escHtml(p.price)}</span>
      </div>
      <div class="muted">–ê–ª–∏–∞—Å: <span class="mono">${escHtml(p.alias)}</span></div>
    `;
  }

  if (short) short.textContent = p.marketplace_ru || "";

  if (specs) {
    const rows = Object.entries(p.specs || {}).map(([k, v]) => {
      if (!v) return "";
      return `<li><strong>${escHtml(k)}:</strong> ${escHtml(v)}</li>`;
    }).join("");

    specs.innerHTML = `<ul class="specs">${rows}</ul>`;
  }

  const desc = document.getElementById("product-desc");
  if (desc) desc.innerHTML = p.desc_ru_html || "";
}

(function initProductPage() {
  const params = new URLSearchParams(location.search);
  const sku = params.get("sku") || PRODUCTS[0].sku;

  const p = getProductBySku(sku);
  renderList(p.sku);
  renderDetails(p);
})();
